/*
01)​ Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, 
subtração, multiplicação e divisão desses valores.
*/

function imprimeResultado(a, b){

    console.log(`A Soma: ${a+b}`)
    console.log(`A Subtração: ${a-b}`)
    console.log(`A Multiplicação: ${a*b}`)
    console.log(`A Divisão: ${a/b}`)
}

imprimeResultado(7, 5)
